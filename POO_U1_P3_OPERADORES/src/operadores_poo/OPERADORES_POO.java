package operadores_poo;


public class OPERADORES_POO {

    
    public static void main(String[] args) {
        //declaramos las variables y las inicializamos
        int a=20;
        int b=15;
        int c=16;
        int d=740;
        
        //Operaciones
       //operacion suma 
       int suma=a+b+c+d;
       //Operacion resta
       int resta=d-c;
       //operacion multiplicacion
       int multiplicacion=d*a;
       //operacion division 
       float division =d/b;
       //Resultados 
       //Imprime el resultado de la suma
       System.out.println("La suma es: " +suma);
       //IMPRIME EL RESULTADO DE LA RESTA
       System.out.println("La resta es:" +resta);
       //IMPRIMER EL RESULTADO DE LA MULTIPLICACION
       System.out.println( "La multiplicacion es: " + multiplicacion);
       //IMPRIME EL RESULTADO DE LA DIVISION 
       System.out.println("La division es: " + division);
       
    }
    
}
